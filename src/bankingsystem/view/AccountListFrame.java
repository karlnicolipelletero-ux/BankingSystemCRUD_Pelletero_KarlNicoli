    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        int selectedRow = tblAccounts.getSelectedRow();

        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select an account from the table first!");
            return;
        }

        int modelRow = tblAccounts.convertRowIndexToModel(selectedRow);

        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblAccounts.getModel();

        String id = model.getValueAt(modelRow, 0).toString();
        String fname = model.getValueAt(modelRow, 1).toString();
        String lname = model.getValueAt(modelRow, 2).toString();
        String email = model.getValueAt(modelRow, 3).toString();
        String phone = model.getValueAt(modelRow, 4).toString();
        String type = model.getValueAt(modelRow, 5).toString();
        String balance = model.getValueAt(modelRow, 6).toString();

        UpdateAccountFrame updateFrame = new UpdateAccountFrame(id, fname, lname, email, phone, type, balance);
        updateFrame.setVisible(true);
        updateFrame.setLocationRelativeTo(null);

        updateFrame.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosed(java.awt.event.WindowEvent e) {
                loadTableData();
            }
        });
    }//GEN-LAST:event_btnUpdateActionPerformed

