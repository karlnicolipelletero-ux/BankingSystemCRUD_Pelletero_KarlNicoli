    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        int selectedRow = tblAccounts.getSelectedRow();

        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select an account from the table to delete!");
            return;
        }

        int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
                "Are you sure you want to permanently delete this account?",
                "Confirm Delete",
                javax.swing.JOptionPane.YES_NO_OPTION,
                javax.swing.JOptionPane.WARNING_MESSAGE);

        if (confirm != javax.swing.JOptionPane.YES_OPTION) {
            return;
        }

        try {

            int modelRow = tblAccounts.convertRowIndexToModel(selectedRow);

            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblAccounts.getModel();
            int accountId = Integer.parseInt(model.getValueAt(modelRow, 0).toString());

            bankingsystem.dao.AccountDAO dao = new bankingsystem.dao.AccountDAO();
            dao.deleteAccount(accountId);

            javax.swing.JOptionPane.showMessageDialog(this, "Account Deleted Successfully!");

            loadTableData();

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

