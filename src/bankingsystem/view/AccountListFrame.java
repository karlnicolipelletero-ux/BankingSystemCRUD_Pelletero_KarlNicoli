    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

        String searchWord = txtSearchKeyword.getText().trim();
        String selectedFilter = cbFilter.getSelectedItem().toString();

        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblAccounts.getModel();
        javax.swing.table.TableRowSorter<javax.swing.table.DefaultTableModel> sorter = new javax.swing.table.TableRowSorter<>(model);
        tblAccounts.setRowSorter(sorter);

        if (searchWord.isEmpty()) {
            sorter.setRowFilter(null);
            return;
        }

        int columnIndex = -1;

        switch (selectedFilter) {
            case "ID":
                columnIndex = 0;
                break;
            case "First Name":
                columnIndex = 1;
                break;
            case "Last Name":
                columnIndex = 2;
                break;
            case "Email":
                columnIndex = 3;
                break;
            case "Phone Number":
                columnIndex = 4;
                break;
        }

        if (columnIndex != -1) {
            sorter.setRowFilter(javax.swing.RowFilter.regexFilter("(?i)" + searchWord, columnIndex));
        }

    }//GEN-LAST:event_btnSearchActionPerformed

